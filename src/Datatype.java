public class Datatype {
        // Datatypes in Java
        // Data types specify the different sizes and values that can be stored in variables. There are 2 types in Java: primitive and non-primitive.
        // Primitive data types: 8 primitive data types are available in java. They are: byte, short, int, long, float, double, char and boolean.
        // Non-primitive data types: Classes, Arrays, Interfaces.

        public static void main(String[] args) {
            // Example of Primitive Data Types
            boolean isValid = true;
            byte a = 10; // 1 byte
            short b = 10000; // 2 bytes
            int c = 100000; // 4 bytes
            long d = 1000000; // 8 bytes
            float fl = 3.142F; 
            double f = 6.76; 
            char letter = 'A'; // 2 bytes
            System.out.println(isValid);
            System.out.println(a);
            System.out.println(b);
            System.out.println(c);
            System.out.println(d);
            System.out.println(fl);
            System.out.println(f);
            System.out.println(letter);
            
        }

    
}
