public class Math {
    // The Java Math has many methods that allows you to perform mathematical tasks on numbers
    
    public static void main(String[] args) {
        // 1: The Math.max(x, y) method allows you to find the highest value of x and y.
        System.out.println(java.lang.Math.max(10, 15));

        // 2: The Math.min(x, y) method allows you to find the lowest value of x and y.
        System.out.println(java.lang.Math.min(20, 55));
        
        // 3: The Math.abs(x) method allows you to find the absolute value of x. This means it will return the positive value of x.
        System.out.println(java.lang.Math.abs(-5));

        // 4: The Math.sqrt(x) method allows you to find the square root of x.
        System.out.println(java.lang.Math.sqrt(64));

        // 5: The Math.pow(x, y) method allows you to raise x to the power of y.
        System.out.println(java.lang.Math.pow(2, 3));

        // 6: The Math.round(x) method allows you to round x to the nearest integer.
        System.out.println(java.lang.Math.round(5.6));

        // 7: The Math.ceil(x) method allows you to round x up to the nearest integer.
        System.out.println(java.lang.Math.ceil(5.6));

        // 8: The Math.floor(x) method allows you to round x down to the nearest integer.
        System.out.println(java.lang.Math.floor(5.6));

        // 9: The Math.random() method allows you to generate a random number between 0 and 1.
        System.out.println(java.lang.Math.random());
    }

}
